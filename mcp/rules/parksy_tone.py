# ─────────────────────────────────────────────────────────────────
#  점수화 함수 — LLM 출력이 박씨 톤에 얼마나 맞는지
# ─────────────────────────────────────────────────────────────────

def tone_score(text: str) -> dict:
    """
    LLM 출력을 박씨 톤 여러 축으로 점수화.
    """
    if not text:
        return {"total": 0.0, "details": {}}

    details = {
        "opener_hit": int(any(text.startswith(o) or f" {o} " in text[:50] for o in PARKSY_OPENERS)),
        "assertive_ending": sum(1 for e in PARKSY_ASSERTIVE_ENDINGS if e in text),
        "imperative": sum(1 for i in PARKSY_IMPERATIVES if i in text),
        "abstraction": sum(1 for v in PARKSY_ABSTRACTION_VOCAB if v in text),
        "econ_frame": sum(1 for e in PARKSY_ECON_FRAME if e in text),
        "profanity": sum(1 for p in PARKSY_PROFANITY if p in text),
    }

    # 가중 합 (박씨 평균 발화 기준으로 튜닝)
    total = (
        details["opener_hit"] * 0.15
        + min(details["assertive_ending"], 5) * 0.10
        + min(details["imperative"], 3) * 0.08
        + min(details["abstraction"], 3) * 0.10
        + min(details["econ_frame"], 3) * 0.08
        + min(details["profanity"], 3) * 0.12
    )

    return {
        "total": round(min(total, 1.0), 3),
        "details": details,
    }


# ─────────────────────────────────────────────────────────────────
#  박씨 톤 강제 치환 (존댓말 → 박씨 톤)
# ─────────────────────────────────────────────────────────────────

TONE_REWRITES: list[tuple[str, str]] = [
    (r"(\S+)합니다\b", r"\1한다"),
    (r"(\S+)입니다\b", r"\1다"),
    (r"(\S+)됩니다\b", r"\1된다"),
    (r"(\S+)세요\b", r"\1"),
    (r"(\S+)주십시오\b", r"\1줘"),
    (r"드리겠습니다\b", "한다"),
    (r"이해\s*합니다\b", "알겠다"),
    (r"공감\s*합니다\b", "그럴 만하다"),
    (r"괜찮습니다\b", "괜찮다"),
    (r"좋습니다\b", "좋다"),
    # 과한 공감 제거
    (r"많이\s*힘드시겠", "힘들겠"),
    (r"잘\s*하고\s*계세요", "잘 하고 있다"),
]


def rewrite_tone(text: str) -> tuple[str, int]:
    """박씨 톤으로 강제 치환. (변환된 텍스트, 치환 수) 반환."""
    count = 0
    result = text
    for pat, repl in TONE_REWRITES:
        new = re.sub(pat, repl, result)
        if new != result:
            count += len(re.findall(pat, result))
        result = new
    return result, count
